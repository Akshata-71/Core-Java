//Concatenation
class Concate
{
	public static void main(String args[])
	{
		System.out.println("Java" + 10);
		System.out.println(10 + "Java");
		System.out.println("Java " + (10 + 20));
		System.out.println((10 + 20 ) + "Java");
		System.out.println("Java" + 10 + 20);
		System.out.println(10 + 20 + "Java");
	}
}